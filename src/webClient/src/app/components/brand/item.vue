                this.$utils.saveItem.call(this, {
                    method: 'brand_update',
                    itemForSaveMod: {},
                    resultModify: this.resultModify,
                })
            },
          reload() {
            let cb = (v) => {
              this.item = this.resultModify(v)
            }
            this.$utils.getDocItemById.call(this, {method: 'brand_get_by_id', cb})
          }
        },
        mounted() {
           this.reload()
        }
    }
</script>
